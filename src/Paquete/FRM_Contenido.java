    private void EditarRegistro(){
    cone2=conex.CargarDB();
    if(cone2!=null){
        try {
            Statement orden = cone2.createStatement();
            String editar = "Update Humanos Set Nombre='"+txtnombre.getText()+"',"
                    +"Apellido'"+txtapellido.getText()+"',Color='"+txtcolor.getText()+"',Peso='"+txtpeso.getText()+"',Altura='"+txtaltura.getText()+"',Direccion='"+txtdireccion.getText()+"',Municipio='"+txtmunicipio.getText()+"', "
                    +"Where Id="+txtid.getText();
            orden.executeUpdate(editar);
            
            JOptionPane.showMessageDialog(this,"Registro Editado Correctamente");
            PropiedadesTabla();
        } catch (SQLException ex) {
            System.out.println("Error:"+ex);
            
        }
    
    }
    }
    private void EliminarRegistro(){
    cone2=conex.CargarDB();
    if (cone2!=null){
        try {
            Statement orden = cone2.createStatement();
            String eliminar = "Delete From Humanos Where Id="+txtid.getText();
            orden.executeUpdate(eliminar);
            orden.close();
            
            JOptionPane.showMessageDialog(this,"Registro eliminado correctamente");
            PropiedadesTabla();
            jComboBox1.removeAllItems();
            ComboItems();
        } catch (SQLException ex) {
            System.out.println("Error:"+ex);
        }
    
    }
    }
